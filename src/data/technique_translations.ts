// Helper function to get English translation
// Handles names with parenthetical clarifications like "Ude-garami (Kimura)"
export function getEnglishName(japaneseName: string): string | null {
  // Try exact match first
  if (techniqueTranslations[japaneseName]) {
    return techniqueTranslations[japaneseName];
  }
  // If name has parens, try matching the part before parens
  const parenIdx = japaneseName.indexOf('(');
  if (parenIdx !== -1) {
    const baseName = japaneseName.substring(0, parenIdx).trim();
    if (techniqueTranslations[baseName]) {
      return techniqueTranslations[baseName];
    }
  }
  return null;
}

// Helper to check if a name looks Japanese
// Returns true for names that have a Japanese structure (dashes, suffixes, etc.)
// and don't contain English clarifying text in parentheses
export function isJapaneseName(name: string): boolean {
  // Skip names that are clearly English-first with Japanese in parentheses
  // e.g., "Shirt grab escape (ude-gatame)"
  if (name.includes(' and ') || name.includes(' or ')) return false;
  if (name.includes('(') || name.includes(')')) {
    // Check if text before parens looks Japanese
    const beforeParen = name.split('(')[0].trim();
    if (isJapaneseCore(beforeParen)) return true;
    // If before parens is English, not Japanese
    return false;
  }
  
  return isJapaneseCore(name);
}

// Core Japanese name detection
function isJapaneseCore(name: string): boolean {
  // Must have at least one Japanese marker
  const hasJP = name.includes('-') || 
    /(dachi|uke|zuki|geri|uchi|kata|waza|nage|gatame|garami|ukemi|komi|barai|kamae|otoshi|gari|goshi|jime|hishigi|gaeshi|hineri|mawashi|seiza|hanten|sasoi|osae|shime|kaiten|kuzushi|zanshin|atemi|kiai|sabaki|tsukuri|hikite|nagashi|kokyu|metsuke|seichusen|nukite|jion|enpi|tekki|heian|seisan|niseishi|rohai|bassai|chinto|kusanku|sanshiryu|ryusan|sanchin|koshi|ippon|morote|jodan)$/i.test(name);
  
  // English words that look Japanese because of hyphens
  const englishHyphenated = ['get-up', 'pop-up', 'push', 'pull', 'basic', 'escape', 'defense', 'layer', 'pass', 'sweeps', 'disarm', 'lock', 'position', 'drag', 'hug', 'smash', 'slice', 'knee', 'single', 'double', 'inside', 'outside', 'take'];
  if (englishHyphenated.some(w => name.toLowerCase().includes(w))) return false;
  
  return hasJP;
}